//The StepCounter class declares a totalSteps property of type Int. 
//This is a stored property with willSet and didSet observers.
class StepCounter {
    var totalSteps: Int = 0 {
        willSet {
            print("About to set totalSteps to \(newValue)")
        }
        didSet {
            if totalSteps > oldValue  {
                print("Added \(totalSteps - oldValue) steps")
            }
        }
    }
}
let stepCounter = StepCounter()
stepCounter.totalSteps = 200
// About to set totalSteps to 200
// Added 200 steps
stepCounter.totalSteps = 360
// About to set totalSteps to 360
// Added 160 steps
stepCounter.totalSteps = 896
// About to set totalSteps to 896
// Added 536 steps

//Global variables are variables that are defined outside of any function, method, closure, or type context.
//Local variables are variables that are defined within a function, method, or closure context.

//Global constants and variables are always computed lazily, in a similar manner to Lazy Stored Properties. 
//Unlike lazy stored properties, global constants and variables do not need to be marked with the lazy modifier.
//Local constants and variables are never computed lazily.

//Stored type properties can be variables or constants.
//Computed type properties are always declared as variable properties, 
//in the same way as computed instance properties.

